    #만약 입력 값이 랜덤 값보다 크면 
        #힌트를 준다. 숫자는 입력 값보다 작다. 출력
        #count를 1 증가시킨다.
        
    #만약 입력 값이 랜덤 값보다 작으면
        #숫자는 입력 값보다 크다. 출력
