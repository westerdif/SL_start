#이용해 주셔서 감사합니다. 출력

import random

game_start = input('시작하시겠습니다.')

random_number = random.randint(1, 31)
count = 0

for i in range(10):
    num = int(input('값을 입력하시오(10번만 입력하실 수 있습니다.) : '))
    
    if num > random_number:
        print("숫자는 입력 값보다 작다.")
    elif num < random_number:
        print("숫자는 입력 값보다 크다.")
    
    else:
        if num == random_number:
            print("000님은 {}번 만에 맞추셨습니다." .format(count))
            print("축하합니다.")
            break
    count += 1

if count == 10:
    print("10번의 시도 동안 정답을 맞추지 못했습니다. 정답은 {}입니다.".format(random_number))

print("이용해 주셔서 감사합니다.")

    
        